<div class="main-topic">
    <h1><b><center>What is sex addiction?</center></b></h1>
</div>
<div class="sub-topic">
    <p><p>Considerable controversy surrounds the diagnosis of “sex addiction.” It’s been excluded from the fifth edition of the “Diagnostic and Statistical Manual of Mental Disorders” (<a href="https://dsm.psychiatryonline.org/doi/book/10.1176/appi.books.9780890425596" target="_blank" rel="noopener noreferrer" class="content-link css-5r4717">DSM-5</a>), but it’s still written about and studied in psychology and counseling circles.</p></p>
    <p>Additionally, <a href="https://onlinelibrary.wiley.com/doi/epdf/10.1111/add.13366" target="_blank" rel="noopener noreferrer" class="content-link css-5r4717">it can still be diagnosed</a> using both DSM-5 (as “Other specified sexual dysfunction”) and the “International Statistical Classification of Diseases and Related Health Problems” (<a href="http://apps.who.int/iris/bitstream/handle/10665/37958/9241544228_eng.pdf?sequence=8&amp;isAllowed=y" target="_blank" rel="noopener noreferrer" class="content-link css-5r4717">ICD-10</a>) criteria (as “Other sexual dysfunction not due to a substance or known physiological condition”).</p>
</div>
<div class="main-topic">
    <h1><b><center>ICD-10 criteria</center></b></h1>
</div>
<div class="sub-topic">
    <p>By way of a definition, “sex addiction” is described as a compulsive need to perform sexual acts in order to achieve the kind of “fix” that a person with <a href="/health/alcohol-use-and-abuse" class="content-link css-5r4717">alcohol use disorder</a> gets from a drink or someone with <a href="/health/opioids-and-related-disorders" class="content-link css-5r4717">opiate use disorder</a> gets from using opiates.</p>
    <p>Sex addiction (the compulsive sexual behavior described here) should not be confused with disorders such as pedophilia or bestiality.</p>
    <p>For some people, sex addiction can be highly dangerous and result in considerable difficulties with relationships. Like drug or alcohol dependence, it has the potential to negatively impact a person’s physical and mental health, personal relationships, quality of life, and safety.</p>
    <p>It’s purported to be somewhat common (although statistics are inconsistent), and some argue that it’s often not diagnosed.</p>
    <p>It’s believed that a person with sex addiction will seek out multiple sex partners, though this in itself is not necessarily a sign of a disorder. Some report that it may manifest itself as a compulsive need to masturbate, view pornography, or be in sexually stimulating situations.</p>
    <p>A person with sex addiction may significantly alter their life and activities in order to perform sexual acts multiple times a day and are reportedly unable to control their behavior, despite severe negative consequences.</p>
</div>
<div class="main-topic">
    <h1><b><center>What are the symptoms of sex addiction?</center></b></h1>
</div>
<div class="sub-topic">
    <p>Since sex addiction isn’t outlined in the DSM-5, there’s considerable controversy about what criteria constitute an addiction.</p>
    <p>One characteristic may be secrecy of behaviors, in which the person with the disorder becomes skilled at hiding their behavior and can even keep the condition secret from spouses, partners, and family members. They may lie about their activities or engage in them at times and places where they won’t be found out.</p>
    <p>But sometimes symptoms are present and noticeable. A person may have a sex addiction if they show some or all of the following signs:</p>
    <ul>
        <li>chronic, obsessive sexual thoughts and fantasies</li>
        <li>compulsive relations with multiple partners, including strangers</li>
        <li>lying to cover behaviors</li>
        <li>preoccupation with having sex, even when it interferes with daily life, productivity, work performance, and so on</li>
        <li>inability to stop or control the behaviors</li>
        <li>putting oneself or others in danger due to sexual behavior</li>
        <li>feeling remorse or guilt after sex</li>
        <li>experiencing other negative personal or professional consequences</li>
    </ul>
    <p>Compulsive behaviors can strain relationships, for example, with the stress of infidelity — although some people may claim to have a sex addiction as a way to explain infidelity in a relationship.</p>
    <p>It’s important to remember that enjoying sexual activity is not a sign of sex addiction. Sex is a healthy human activity, and enjoying it is normal. In addition, differences in the level of sexual interest between partners does not mean that one partner has a sex addiction.</p>
</div>
<div class="main-topic">
    <h1><b><center>What are the treatments for sex addiction?</center></b></h1>
</div>
<div class="sub-topic">
    <p>Since the diagnosis is controversial, evidence-based treatment options are lacking.</p>
    <p> Those who describe treating sex addiction may recommend one or more of the following methods.</p>
    <h4><b>Inpatient treatment programs</b></h4>
    <p>There are many inpatient treatment centers that offer sex addiction recovery programs. Often, people with a sex addiction are removed from their normal daily lives for at least 30 days to help them regain control of their impulses and start healing. These types of programs typically include in-depth individual and group therapy sessions.</p>
    <h4><b>12-step programs</b></h4>
    <p>Programs such as Sex Addicts Anonymous (SAA) follow the same recovery model as Alcoholics Anonymous (AA). They can be very helpful for addressing sex addiction.</p>
    <p>Members aren’t required to give up sex entirely, but they are encouraged to refrain from compulsive and destructive sexual behavior. Group meetings with others addressing the same challenges provide a good support system.</p>
    <h4><b>Medication</b></h4>
    <p>Some people may benefit from a course of drug therapy. Certain antidepressants might help alleviate urges (which is separate from the potential side effects of some antidepressants that can cause decreased libido or impair other aspects of the sexual experience).</p>
    <p>It’s not clear, however, whether a physician would prescribe drugs for this condition.</p>
</div>
<div class="main-topic">
    <h1><b><center>What’s the outlook for sex addiction?</center></b></h1>
</div>
<div class="sub-topic">
    <p>The person addressing sex addiction faces a unique set of challenges. They may be engaging in behaviors that put their relationships, their own safety and health, and the health of their partner in jeopardy. At the same time, sex addiction is considered a controversial diagnosis and it’s lacking diagnostic criteria as well as evidence-based treatments.</p>



