    <h1><b><center>Cocaine And Addiction</center></b></h1>
</div>
<div class="sub-topic">
    <p>Cocaine is a drug made from the leaves of the coca plant native to South America.
        According to the National Institute on Drug Abuse, about 15 percent of people in
        the United States have tried cocaine.</p>
    <p>Cocaine is a stimulant, meaning it increases alertness and energy. It affects the
        neuropathways in your brain, leading you to feel talkative, energetic, and euphoric.
        Addiction to cocaine can develop quickly, even after trying it only a few times.</p>
    <p>An addiction can be physical, meaning your body craves the drug. It can also be mental,
        meaning you strongly desire the drugâ€™s effects.</p>
</div>
<div class="main-topic">
    <h1><b><center>What are the effects of cocaine?</center></b></h1>
</div>
<div class="sub-topic">
    <p> a short time, cocaine has stimulating effects on the body. It causes a naturally
        occurring neurotransmitter called dopamine to increase its concentration in the brain.
        This causes feelings of pleasure and satisfaction. Cocaine causes your dopamine levels
        to rise causing the user to feel euphoric.</p>
    <p>Cocaine prevents the dopamine, and other neurotransmitters norepinephrine and serotonin,
        from being taken up into the nerve cells. This allows large amounts of the neurotransmitters
        to accumulate and stimulate the surrounding nerve cells. This heightens the pleasurable
        sense of euphoria.</p>
    <p>Cocaine can also minimize your desire for sleep and food. Some people report that cocaine
        helps them think and perform tasks more quickly. Many users begin to crave the feelings
        that cocaine creates.</p>
    <p>
        Psychological effects of cocaine addiction include:</p>
    <ul>
        <li>paranoia</li>
        <li>panic</li>
        <li>hallucinations</li>
        <li>aggression</li>
        <li>irritability</li>
        <li>anxiety</li>
        <li>depression</li>
        <li>impaired judgment</li>
        <li>repetitive or abnormal behaviors</li>
    </ul>
    <p>Physical effects of cocaine addiction include:</p>
    <ul>
        <li>unhealthy weight loss</li>
        <li>increased heart rate</li>
        <li>nausea</li>
        <li>abdominal pain</li>
        <li>headaches</li>
        <li>chest pain</li>
        <li>heart arrhythmia </li>
        <li>heart attack</li>
        <li>seizure</li>
        <li>stroke</li>
    </ul>
</div>
<div class="main-topic">
    <h1><b><center>What causes cocaine addiction?</center></b></h1>
</div>
<div class="sub-topic">
    <p>Cocaine affects neurological systems in your brain. Cocaine use, especially repetitive use, can alter systems associated with pleasure, memory, and decision making. When someone is addicted, their ability to resist urges becomes impaired, making it harder to quit.</p>
    <p>All stimulants act to enhance the extracellular concentrations of dopamine, norepinephrine, and serotonin. Stimulant drugs can block the transport of these neurotransmitters.</p>
    <p>Repeated exposure to cocaine results in neuroadaptation. This includes sensitization (increased drug response) and tolerance (decreased drug response). Physical tolerance to the effects of cocaine can occur after just a few uses. This results in needing more and more of the drug to get the same effect.</p>
</div>
<div class="main-topic">
    <h1><b><center>What are the symptoms of cocaine addiction?</center></b></h1>
</div>
<div class="sub-topic">
    <p>Symptoms of a cocaine addiction include:</p>
    <ul>
        <li>a tolerance for the drug, requiring large amounts to get high</li>
        <li>an inability to stop or reduce usage</li>
        <li>withdrawal symptoms when usage stops</li>
        <li>a desire to keep using even when health complications arise</li>
        <li>a negative impact on quality of life, relationships, and employment</li>
        <li>spending excessive time and money looking for cocaine</li>
        <li>psychosis and hallucinations</li>
        <li>irritability or anxiety</li>
        <li>disappearing for binge sessions</li>
    </ul>
</div>
<div class="main-topic">
    <h1><b><center>How is cocaine addiction treated?</center></b></h1>
</div>
<div class="sub-topic">
    <p>Cocaine addiction is a complex disease, with physical, mental, social, environmental, and familial factors. There are a variety of treatment methods for cocaine addiction that address all these components.</p>
    <h4><b>Treatment facilities</b></h4>
    <p>Residential treatment programs work to cover all facets of addiction. These programs can last for several weeks to a year. And they often include support groups, vocational rehab, or therapy.</p>
    <h4><b>Behavior treatment</b></h4>
    <p>Behavior treatments show promising results for helping people through cocaine addiction. Treatment can be done on an outpatient basis or as part of a residential treatment program. Interventions focusing on behavior are often used along with medications.</p>
    <p>Behavior treatments include rewards for meeting goals related to stopping use, and cognitive behavioral therapy, which teaches learning processes that help you continue not using.</p>
    <h4><b>Medications</b></h4>
    <p>While there are no medications designed specifically to treat cocaine addiction, some medications with other purposes can be helpful, such as antidepressants.</p>
    <h4><b>Alternative therapies</b></h4>
    <p>Other solutions to help overcome cocaine addiction include exercise, hypnosis, acupuncture, and herbs. But more research is required to determine the effectiveness of these techniques on addiction to cocaine.</p>
</div>
<div class="main-topic">
    <h1><b><center>What are the effects of cocaine withdrawal?</center></b></h1>
</div>
<div class="sub-topic">
    <p>Addicted users who stop using cocaine will undergo an initial crash, known as withdrawal. Withdrawal can be intense and difficult due to cravings and uncomfortable side effects. Effects of cocaine withdrawal include</p>
    <ul>
        <li>fatigue</li>
        <li>hostility</li>
        <li>paranoia</li>
        <li>anxiety</li>
        <li>agitation</li>
        <li>sleep disturbances</li>
        <li>depression</li>
    </ul>
    <p>Withdrawal from cocaine can cause intense discomfort. And this can cause a strong desire to use the drug again. Even when withdrawal symptoms have subsided, sudden cravings are common.</p>
    <p>Support systems such as friends, family, treatment facilities, and other people recovering from addiction, can help you push through this phase. The organization <a href="https://ca.org/" target="_blank" class="content-link css-5r4717">Cocaine Anonymous</a> offers resources and a community for people recovering from cocaine addiction. </p>
    <p>To find a support group in your area, talk to a healthcare provider or go online.</p>
</div>
<div class="main-topic">
    <h1><b><center>What is the outlook for cocaine addiction?</center></b></h1>
</div>
<div class="sub-topic">
    <p>Cocaine addiction is a complex illness that requires treatment. Cocaine addiction has a serious impact on your mental and physical health, and can result in premature death.</p>
    <p>If you or a loved one is addicted to cocaine, reach out to a doctor, or someone else you trust who may be able help you explore treatment options and find other sources of support.</p>
</div>



